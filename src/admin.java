    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            stmt=conn.createStatement();
            int prId=Integer.parseInt(idPr.getText());
            String sql="SELECT * FROM ADMIN WHERE ID= '"+prId+" '";
            rs=stmt.executeQuery(sql);
            if(rs.next()){
                numePr.setText(rs.getString("NUME"));
                adresa.setText(rs.getString("MAIL"));
                
                parola.setText(rs.getString("PASSWORD"));
                
            }
            else
            JOptionPane.showMessageDialog(null,"Profesorul nu a fost gasit");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

