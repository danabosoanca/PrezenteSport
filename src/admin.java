    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            stmt=conn.createStatement();
            int prId=Integer.parseInt(idPr.getText());
            String prNume=numePr.getText();
            String prAdresa=adresa.getText();
            
            String prParola=parola.getText();
            

            String sql="UPDATE ADMIN SET    NUME='"+prNume+"',MAIL='"+prAdresa+"', PASSWORD='"+prParola+"'WHERE id= '"+prId+"'";

            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Datele au fost modificate");

        }catch(HeadlessException | NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

