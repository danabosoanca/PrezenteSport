    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            stmt=conn.createStatement();
            int stdId=Integer.parseInt(id.getText());
            String sql="SELECT * FROM STUDENT WHERE ID= '"+stdId+" '";
            rs=stmt.executeQuery(sql);
            if(rs.next()){
                numeSt.setText(rs.getString("NUME"));
                facultate.setText(rs.getString("FACULTATE"));
                an.setText(String.format("%s",rs.getInt("AN")));
                sport.setText(rs.getString("SPORT"));
                prezente.setText(String.format("%s",rs.getInt("PREZENTE")));
                
            }
            else
                JOptionPane.showMessageDialog(null,"Studentul nu a fost gasit");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
       
        
         
    }//GEN-LAST:event_jButton3ActionPerformed

