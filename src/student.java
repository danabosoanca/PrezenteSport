            String stdFacultate=facultate.getText();
            int stdAn=Integer.parseInt(an.getText());
            String stdSport=sport.getText();
            int stdPrezente=Integer.parseInt(prezente.getText());

            String sql="UPDATE STUDENT SET    NUME='"+stdNume+"',FACULTATE='"+stdFacultate+"', AN='"+stdAn+"',SPORT='"+stdSport+"',PREZENTE='"+stdPrezente +"'WHERE id= '"+stdId+"'";

